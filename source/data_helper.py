def _get_initial_data(func_no):
    assert type(func_no) == int and 1 <= func_no and func_no <= 8, 'arg func_no must be interger between 1 and 8 inclusive' 

    INITIAL_DATA_PATH = 'Initial Data/'
    
    initial_inputs  = f'{INITIAL_DATA_PATH}initial_inputs{func_no}.npy'
    initial_outputs = f'{INITIAL_DATA_PATH}initial_outputs{func_no}.npy'
    
    with open(initial_inputs, 'rb') as f:
        x_initial = np.load(f)

